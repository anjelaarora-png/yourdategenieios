export function getPlaybookComboKey(
  gender: string | null | undefined,
  partnerGender: string | null | undefined
): PlaybookComboKey {
  const g = (gender || "").toLowerCase();
  const p = (partnerGender || "").toLowerCase();
  if (g === "male" && p === "female") return "male_female";
  if (g === "female" && p === "male") return "female_male";
  if (g === "male" && p === "male") return "male_male";
  if (g === "female" && p === "female") return "female_female";
  return "default";
}

const MIN_TIPS = 50;

export function getPlaybookTips(categoryId: string, comboKey: PlaybookComboKey): string[] {
  const category = PLAYBOOK_TIPS[categoryId];
  if (!category) return [];
  const fallback = category.default ?? [];
  const tips = category[comboKey];
  if (!tips || tips.length < MIN_TIPS) return [...fallback];
  return [...tips];
}

